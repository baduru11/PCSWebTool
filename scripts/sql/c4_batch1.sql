布的蓝天。星光在我们的肉眼里虽然微小，然而它使我们觉得光明无处不在。那时候我正在读一些天文学的书，
也认得一些星星，好像它们就是我的朋友，它们常常在和我谈话一样。如今在海上，每晚和繁星相对，我把它们认得很熟了。我躺在舱面上，仰望天空。深蓝色的天空里悬着无数半明
半昧的星。船在动，星也在动，它们是这样低，真是摇摇欲坠呢！渐渐地我的眼睛模糊了，我好像看见无数萤
火虫在我的周围飞舞。海上的夜是柔和的，是静寂的，是梦幻的。我望着许多认识的星，我仿佛看见它们在对
我眨眼，我仿佛听见它们在小声说话。这时我忘记了一切。在星的怀抱中我微笑着，我沉睡着。我觉得自己是
一个小孩子，现在睡在母亲的怀里了。
有一夜，那个在哥伦波上船的英国人指给我看天上的巨人。他用手指着：那四颗明亮的星是头，下面的几颗是
身子，这几颗是手，那几颗是腿和脚，还有三颗星算是腰带。经他这一番指点，我果然看清楚了那个天上的巨
人。看，那个巨人还在跑呢！
